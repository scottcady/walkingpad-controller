    static let md: CGFloat = 16

    /// 24pt - Comfortable spacing
    static let lg: CGFloat = 24

    /// 32pt - Spacious
    static let xl: CGFloat = 32

    /// 48pt - Extra spacious
    static let xxl: CGFloat = 48

    /// 64pt - Maximum spacing
    static let xxxl: CGFloat = 64
